function showDetails(lessonId, index) {
  const lessonWords = lessons[lessonId].words;
  const wordData = lessonWords[index];

  document.getElementById('modal-word').textContent = wordData.word;
  document.getElementById(
    'modal-meaning'
  ).textContent = `"${wordData.meaning}"`;
  document.getElementById('modal-example').textContent = wordData.example;
  document.getElementById('modal-synonyms').textContent =
    wordData.synonyms.join(', ');

  modalContainer.classList.remove('hidden');
}

function closeModal() {
  modalContainer.classList.add('hidden');
}
